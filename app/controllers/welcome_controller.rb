    @food_basics = "Food Basics:
All food is to be weighed RAW

Protein:
Lean Proteins:
Skinless Chicken Breast
Egg Whites
Tilapia, Cod, Red Snapper
Porkchops or Porkloin with Fat trimmed off
95% lean beef or turkey
Protein Powder

Fatty Proteins:
For these you can have pretty much anything not mentioned in the Lean Protein List; just don't go for something like extra fatty pork belly, here's some good choices:
Salmon (Omega 3's!)
Steak
90% lean beef or turkey.

1 Portion of Protein is equal to:
125g of Chicken, Fish, Beef or Turkey (MUST be weighed raw)
1 Scoop of Protein Supplement
1 Cup of Egg Whites

Carbs:
Best Carbs:
Oats
Brown Rice
Sweet Potatoes or Yams
Potatoes

Good Carbs:
White Rice
Whole Grain Breads
Whole Wheat Pasta / Regular Pasta

1 Portion of Carbs is equal to:
30g of any Rice, Oats or Pasta
130g Sweet or regular Potato
1 Slice of any Bread under 120kcal (2 slices if they are 60kcal or less)

Fats:
Most of our fats are going to come from fatty meats, fish oil supplement, whatever small amounts of oil we use to cook our foods, and the occasional spoonful of peanut butter.

Top Fats:
Omega 3 (from capsules)
Nuts & Natural Peanut Butter
Coconut Oil

On training days you will eat Lean Proteins only;  on rest days, you can have fattier protein for two meals, or you can also just have lean proteins and add a tablespoon of Coconut Oil or a tablespoon of Natural Peanut Butter to replace the fats in that meal.

Vegetables:
Veggies are not optional, and I recommend you have them in two of your meals, you can have as much as you want (with a few exceptions), try to use very little oil when cooking them.

Here's a list of my favorites:
Zucchini, Cucumber, Broccoli, Cabbage, Spinach, Lettuce, Kale, Peppers, Green Beans, Brussel Sprouts, Mushrooms.

These ones are not okay unless you count the carbs in them:
Potatoes, Carrots, Corn, Peas and Parsnips

Notes:
Adding any condiments is fine, but I would forego any salad dressing unless it is very low cal, and you control the amount to less than 50kcals.

Coconut Oil cooking spray and \"I Can't Believe it's Not Butter\" spray are great for these as well.

Frozen Veggies are totally fine, just make sure they don't have sauce on them, the microwaveable ones are especially convenient.

This is my favorite option for the easiest and most convenient vegetable blend, you get it at Costco; just stir fry some and you've got a fantastic blend in terms of nutrition and diversity.

Fruit:
No fruit juices or dried fruit allowed, as we lose many of the benefits when they are processed.

1 Portion of Fruit is equal to:
1 Medium Banana or Apple
1.5 Cups of Raspberries, Strawberries or Blackberries  (220g)

1 Cup of Blueberries or Grapes

If your favorite fruit isn't listed here, let me know and I'll tell you what the portion is.

Snacks:

Anything with 100kcals or less is okay, don't feel like you have to choose something \"healthy\".

I love ice cream, so I choose ice cream options, my favorites are Yasso Yogurt Bars, or just Fudge Bars.
Dannon Light and Fit Yogurts are also great."
